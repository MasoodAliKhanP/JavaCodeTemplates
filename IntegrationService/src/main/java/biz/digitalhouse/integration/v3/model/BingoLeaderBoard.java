package biz.digitalhouse.integration.v3.model;

/**
 * @author Vitalii Babenko
 *         on 04.03.2016.
 */
public class BingoLeaderBoard {

    private String extPlayerId;
    private String nickname;
    private int numberOfWonCards;
    private double totalAmount;
    private String currencySymbol;

    public BingoLeaderBoard(String extPlayerId, String nickname, int numberOfWonCards, double totalAmount, String currencySymbol) {
        this.extPlayerId = extPlayerId;
        this.nickname = nickname;
        this.numberOfWonCards = numberOfWonCards;
        this.totalAmount = totalAmount;
        this.currencySymbol = currencySymbol;
    }

    public String getExtPlayerId() {
        return extPlayerId;
    }

    public String getNickname() {
        return nickname;
    }

    public int getNumberOfWonCards() {
        return numberOfWonCards;
    }

    public double getTotalAmount() {
        return totalAmount;
    }

    public String getCurrencySymbol() {
        return currencySymbol;
    }

    @Override
    public String toString() {
        return "BingoLeaderBoard{" +
                "extPlayerId='" + extPlayerId + '\'' +
                ", nickname='" + nickname + '\'' +
                ", numberOfWonCards=" + numberOfWonCards +
                ", totalAmount=" + totalAmount +
                ", currencySymbol=" + currencySymbol +
                '}';
    }
}

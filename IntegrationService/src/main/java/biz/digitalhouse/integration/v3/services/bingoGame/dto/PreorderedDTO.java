package biz.digitalhouse.integration.v3.services.bingoGame.dto;

import java.util.Calendar;

/**
 * @author Vitalii Babenko
 *         on 03.03.2016.
 */
public class PreorderedDTO {

    private Calendar preorderedDate;
    private Calendar sessionStartDate;
    private long bingoGameID;
    private long bingoRoomID;
    private long roundID;
    private double cardCost;
    private int cardNumbers;
    private String purchaseMode;
    private String bingoGameName;
    private String bingoRoomName;
    private int bingoType;
    private String currencySymbol;

    public PreorderedDTO(Calendar preorderedDate, Calendar sessionStartDate, long bingoGameID, long bingoRoomID, long roundID,
                         double cardCost, int cardNumbers, String purchaseMode, String bingoGameName, String bingoRoomName, int bingoType,
                         String currencySymbol) {

        this.preorderedDate = preorderedDate;
        this.sessionStartDate = sessionStartDate;
        this.bingoGameID = bingoGameID;
        this.bingoRoomID = bingoRoomID;
        this.roundID = roundID;
        this.cardCost = cardCost;
        this.cardNumbers = cardNumbers;
        this.purchaseMode = purchaseMode;
        this.bingoGameName = bingoGameName;
        this.bingoRoomName = bingoRoomName;
        this.bingoType = bingoType;
        this.currencySymbol = currencySymbol;
    }

    public Calendar getPreorderedDate() {
        return preorderedDate;
    }

    public Calendar getSessionStartDate() {
        return sessionStartDate;
    }

    public long getBingoGameID() {
        return bingoGameID;
    }

    public long getBingoRoomID() {
        return bingoRoomID;
    }

    public long getRoundID() {
        return roundID;
    }

    public double getCardCost() {
        return cardCost;
    }

    public int getCardNumbers() {
        return cardNumbers;
    }

    public String getPurchaseMode() {
        return purchaseMode;
    }

    public String getBingoGameName() {
        return bingoGameName;
    }

    public String getBingoRoomName() {
        return bingoRoomName;
    }

    public int getBingoType() {
        return bingoType;
    }

    public String getCurrencySymbol() {
        return currencySymbol;
    }

    public void setCurrencySymbol(String currencySymbol) {
        this.currencySymbol = currencySymbol;
    }

    @Override
    public String toString() {
        return "PreorderedDTO{" +
                "preorderedDate=" + preorderedDate +
                ", sessionStartDate=" + sessionStartDate +
                ", bingoGameID=" + bingoGameID +
                ", bingoRoomID=" + bingoRoomID +
                ", roundID=" + roundID +
                ", cardCost=" + cardCost +
                ", cardNumbers=" + cardNumbers +
                ", purchaseMode='" + purchaseMode + '\'' +
                ", bingoGameName='" + bingoGameName + '\'' +
                ", bingoRoomName='" + bingoRoomName + '\'' +
                ", bingoType=" + bingoType +
                ", currencySymbol=" + currencySymbol +
                '}';
    }
}

package biz.digitalhouse.integration.v3.web.ws.http.internalAPIEndpoint.dto;

import java.util.ArrayList;
import java.util.List;

public class BulkBalanceResponse extends BaseResponse {

    private List<PlayerBalance> balanceList;

    public List<PlayerBalance> getBalanceList() {
        if (balanceList == null) {
            balanceList = new ArrayList<PlayerBalance>();
        }
        return this.balanceList;
    }
}

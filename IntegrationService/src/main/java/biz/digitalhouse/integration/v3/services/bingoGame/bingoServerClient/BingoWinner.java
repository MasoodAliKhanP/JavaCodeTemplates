package biz.digitalhouse.integration.v3.services.bingoGame.bingoServerClient;

import java.io.Serializable;

/**
 * Created by vitaliy.babenko
 * on 31.10.2016.
 */
public class BingoWinner implements Serializable {

    private int partNumber;
    private String winners;
    private String pattern;
    private int winningCallNumber;

    public int getPartNumber() {
        return partNumber;
    }

    public void setPartNumber(int partNumber) {
        this.partNumber = partNumber;
    }

    public String getWinners() {
        return winners;
    }

    public void setWinners(String winners) {
        this.winners = winners;
    }

    public String getPattern() {
        return pattern;
    }

    public void setPattern(String pattern) {
        this.pattern = pattern;
    }

    public int getWinningCallNumber() {
        return winningCallNumber;
    }

    public void setWinningCallNumber(int winningCallNumber) {
        this.winningCallNumber = winningCallNumber;
    }

    @Override
    public String toString() {
        return "BingoWinner{" +
                "partNumber=" + partNumber +
                ", winners='" + winners + '\'' +
                ", pattern='" + pattern + '\'' +
                ", winningCallNumber=" + winningCallNumber +
                '}';
    }
}

package biz.digitalhouse.integration.v3.services.externalClient.oldHttpResponse;

/**
 * <p>Class: BetResponse</p>
 * <p>Description: </p>
 *
 * @author Sergey Miliaev
 */

public class BetResponse extends BaseTransactionalResponse {

    private String currency;
    private Double cash;
    private Double bonus;
    private Double usedBonus;

    public String getCurrency() {
        return currency;
    }

    public void setCurrency(String currency) {
        this.currency = currency;
    }

    public Double getCash() {
        return cash;
    }

    public void setCash(Double cash) {
        this.cash = cash;
    }

    public Double getBonus() {
        return bonus;
    }

    public void setBonus(Double bonus) {
        this.bonus = bonus;
    }

    public Double getUsedBonus() {
        return usedBonus;
    }

    public void setUsedBonus(Double usedBonus) {
        this.usedBonus = usedBonus;
    }

    @Override
    public String toString() {
        return "BetResponse{" +
                "currency='" + currency + '\'' +
                ", cash=" + cash +
                ", bonus=" + bonus +
                ", usedBonus=" + usedBonus +
                "} " + super.toString();
    }
}

package biz.digitalhouse.integration.v3.web.ws.http.internalAPIEndpoint.dto;

public class JackpotWinRequest extends BaseRequest {

    private String transactionID;
    private long jackpotID;
    private String tierTypeID;
    private long playerID;
    private String gameSymbol;
    private String roundID;
    private double win;
    private String originalRoundID;
    private boolean endRound;
    
    public String getTransactionID() {
        return transactionID;
    }

    public void setTransactionID(String value) {
        this.transactionID = value;
    }

    public long getJackpotID() {
        return jackpotID;
    }

    public void setJackpotID(long value) {
        this.jackpotID = value;
    }

    public long getPlayerID() {
        return playerID;
    }

    public void setPlayerID(long value) {
        this.playerID = value;
    }

    public String getGameSymbol() {
        return gameSymbol;
    }

    public void setGameSymbol(String value) {
        this.gameSymbol = value;
    }

    public String getRoundID() {
        return roundID;
    }

    public void setRoundID(String value) {
        this.roundID = value;
    }

    public double getWin() {
        return win;
    }

    public void setWin(double value) {
        this.win = value;
    }

    public String getOriginalRoundID() {
        return originalRoundID;
    }

    public void setOriginalRoundID(String value) {
        this.originalRoundID = value;
    }
    
    public boolean isEndRound() {
		return endRound;
	}

	public void setEndRound(boolean endRound) {
		this.endRound = endRound;
	}

	public String getTierTypeID() {
		return tierTypeID;
	}

	public void setTierTypeID(String tierTypeID) {
		this.tierTypeID = tierTypeID;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "JackpotWinRequest [transactionID=" + transactionID + ", jackpotID=" + jackpotID + ", playerID="
				+ playerID + ", gameSymbol=" + gameSymbol + ", roundID=" + roundID + ", win=" + win
				+ ", originalRoundID=" + originalRoundID + ", endRound=" + endRound + "]";
	}

}

package biz.digitalhouse.integration.v3.web.ws.http.internalAPIEndpoint.dto;

public class BaseResponse {

    private int status;

    BaseResponse(int status){
    	this.status = status;
    }
    
    public BaseResponse() {
		super();
	}

	public int getStatus() {
        return status;
    }

    public void setStatus(int value) {
        this.status = value;
    }

    @Override
    public String toString() {
        return "BaseResponse{" +
                "status=" + status +
                '}';
    }
}

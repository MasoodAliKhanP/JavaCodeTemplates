package biz.digitalhouse.integration.v3.services.userservice;

/**
 * @author vitalii.babenko
 * created: 05.04.2018 13:05
 */
public class CreateExternalPlayerRequest {

    private String nickname;
    private String email;
    private String firstName;
    private String lastName;
    private String currency;
    private String language;

    public CreateExternalPlayerRequest(String nickname, String email, String firstName, String lastName, String currency, String language) {
        this.nickname = nickname;
        this.email = email;
        this.firstName = firstName;
        this.lastName = lastName;
        this.currency = currency;
        this.language = language;
    }

    public String getNickname() {
        return nickname;
    }

    public String getEmail() {
        return email;
    }

    public String getFirstName() {
        return firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public String getCurrency() {
        return currency;
    }

    public String getLanguage() {
        return language;
    }

    @Override
    public String toString() {
        return "CreateExternalPlayerRequest{" +
                "nickname='" + nickname + '\'' +
                ", email='" + email + '\'' +
                ", firstName='" + firstName + '\'' +
                ", lastName='" + lastName + '\'' +
                ", currency='" + currency + '\'' +
                ", language='" + language + '\'' +
                '}';
    }
}
